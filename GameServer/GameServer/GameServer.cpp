#include "pch.h"
#include <iostream>


//마이크로소프트에서 제공하는 소켓 라이브러리
#include <WinSock2.h>
#include <MSWSock.h>
#include <ws2tcpip.h>
#pragma comment(lib, "ws2_32.lib")

void HandleError(const char* cause) {
	int32 errCode = ::WSAGetLastError();
	cout << cause << "ErrorCode" << errCode << endl;

}

int main()
{
	//save("GameServer.cpp", "ServerCodeFile.txt");
	cout << "Server" << endl;


	WSADATA wsData;
	if (::WSAStartup(MAKEWORD(2, 2), &wsData))
		return 0;

	//블로킹(Blocking) 소켓
	// accept -> 접속한 클라가 있을때
	//connet-> 서버 접속 성공했을때
	//send , sendto -> 요청한 데이터를 송신 버퍼에 복사했을때
	//recv, recvfrom -> 수신 버퍼에 도착한 데이터가 있고, 이를 유저 레벨 버퍼에 복사했을때
	
	//논 블로킹(Non- Blocking)

	SOCKET listenSocket = ::socket(AF_INET, SOCK_STREAM, 0);
	if (listenSocket == INVALID_SOCKET)
	{
		return 0;
	}


	u_long on = 1;
	if (::ioctlsocket(listenSocket, FIONBIO, &on) == INVALID_SOCKET)
		return 0;
	
	
	SOCKADDR_IN serverAddr;
	::memset(&serverAddr, 0, sizeof(serverAddr));
	serverAddr.sin_family = AF_INET;
	serverAddr.sin_addr.s_addr = htonl(INADDR_ANY);
	serverAddr.sin_port = htons(7777);


	if (::bind(listenSocket, (SOCKADDR*)&serverAddr, sizeof(serverAddr)) == SOCKET_ERROR)
		return 0;


	if (::listen(listenSocket, SOMAXCONN) == SOCKET_ERROR)
		return 0;


		SOCKADDR_IN clientAddr;
		int32 addrLen = sizeof(clientAddr);


		//Accept
		while (true) {

			SOCKET clientSocket = ::accept(listenSocket, (SOCKADDR*)&clientAddr, &addrLen);
			if (clientSocket == INVALID_SOCKET) {

				//원래 블록 했어야했는데...너가 논블로킹으로 하라며
				if (::WSAGetLastError() == WSAEWOULDBLOCK)
					continue;

				//Error
				break;
			}

			cout << "client Conneted!" << endl; 

			//recv
			while (true) {

				char recvBuffer[1000];
				int32 recvLen = ::recv(clientSocket, recvBuffer, sizeof(recvBuffer), 0);

				if (recvLen == SOCKET_ERROR) {

					if (::WSAGetLastError() == WSAEWOULDBLOCK)
						continue;

					break;
				}
				else if (recvLen == 0) {
					//연결 끊어짐
					break;
				}

				cout << "Recv Data Len = " << recvLen << endl;

				//send
				while (true) {

					if (::send(clientSocket, recvBuffer, recvLen, 0) == SOCKET_ERROR) {


						//원래 블록 했어야했는데.. 너가 논 블로킹으로 하라며,...
						if (::WSAGetLastError() == WSAEWOULDBLOCK)
							continue;

						//Error
						break;
					}

					cout << "send Data! Len = " << recvLen << endl;
				}
			}
			
		}

	//윈속 종료.
	::WSACleanup();
}

